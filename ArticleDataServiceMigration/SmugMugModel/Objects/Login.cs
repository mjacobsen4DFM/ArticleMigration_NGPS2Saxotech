using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace SmugMugModel
{
    public class Login
    {
        #region Properties
        /// <summary>
        /// The account status for this user. Values: Active, Expired
        /// </summary>
        public AccountStatusEnum AccountStatus { get; set; }
        /// <summary>
        /// The account type for this user. Values: Pro, Power, Standard
        /// </summary>
        public AccountTypeEnum AccountType { get; set; }
        /// <summary>
        /// The file size limit for this user's account.
        /// </summary>
        public int FileSizeLimit { get; set; }
        /// <summary>
        /// The password hash for this user.
        /// </summary>
        public string PasswordHash { get; set; }
        /// <summary>
        /// Session struct for the SessionID
        /// </summary>
        public Session Session { get; set; }
        /// <summary>
        /// SmugVault is enabled or not for this user's account
        /// </summary>
        public bool SmugVault { get; set; }
        /// <summary>
        /// User struct including Id, DisplayName, NickName and URL
        /// </summary>
        public User User { get; set; }
        #endregion

        public Login()
        {
            Session = new Session();
            User = new User();
        }


        /// <summary>
        /// Fill the current user with the necessary info
        /// </summary>
        /// <param name="u"></param>
        public void PopulateUser(MyUser u)
        {
            u.basic.SessionID = Session.id;
            u.basic.NickName = User.NickName;
            u.DisplayName = User.DisplayName;
            u.UserId = User.id;
            u.FileSizeLimit = FileSizeLimit;
            u.AccountType = AccountType.ToString();
            u.AccountStatus = AccountStatus.ToString();            
            u.SmugVault = SmugVault;
            u.PasswordHash = PasswordHash;
            u.URL = User.URL;
        }
    }
}
